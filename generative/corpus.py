def get_full_corpus() -> str:
    """Returns the full corpus as a single string."""
    parts = []
    for domain, sentences in CORPUS_DOMAINS.items():
        parts.extend(sentences)
    # Add extended sentences
    try:
        parts.extend(_generate_extended_sentences())
    except NameError:
        pass  # _generate_extended_sentences not yet defined when first loaded
    return ". ".join(parts) + "."


def get_corpus_sentences() -> list[str]:
    """Returns the corpus as a list of sentences."""
    sentences = []
    for domain_sents in CORPUS_DOMAINS.values():
        sentences.extend(domain_sents)
    try:
        sentences.extend(_generate_extended_sentences())
    except NameError:
        pass
    return sentences


def get_corpus_stats() -> dict:
    """Returns statistics about the corpus."""
    sentences = get_corpus_sentences()
    words = sum(len(s.split()) for s in sentences)
    return {
        "n_domains": len(CORPUS_DOMAINS),
        "n_sentences": len(sentences),
        "n_words": words,
        "avg_sentence_length": words / max(1, len(sentences)),
        "domains": list(CORPUS_DOMAINS.keys()),
    }


